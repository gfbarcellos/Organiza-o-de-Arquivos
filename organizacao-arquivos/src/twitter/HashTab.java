package twitter;

public class HashTab {
	
	public String data;
	public String Indice;
	
	public HashTab(){
		this.data = "0";
		Indice = "0";
	}
	
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getIndice() {
		return Indice;
	}
	public void setIndice(String indice) {
		Indice = indice;
	}	
	
}
